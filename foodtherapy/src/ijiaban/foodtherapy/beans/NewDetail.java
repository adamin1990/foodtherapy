package ijiaban.foodtherapy.beans;

import java.io.Serializable;
import java.util.Date;

/**
 * 资讯详情
 * http://api.yi18.net/news/show?id=7090
 * @author adamin
 *
 */
public class NewDetail implements Serializable{
	private static final long serialVersionUID = 1L;
	private long id;//资讯id
	private String title; //资讯标题
	private String tag ;//资讯标签 tag
	private String message ;//资讯 详细内容
	private String img ;//图片
	private int count ;//浏览此时
	private String author; //作者
	private int focal;  //是否焦点 
	private Date time ;//发布时间 
	public long getId() {
		return id;
	}
	public void setId(long id) {
		this.id = id;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getTag() {
		return tag;
	}
	public void setTag(String tag) {
		this.tag = tag;
	}
	public String getMessage() {
		return message;
	}
	public void setMessage(String message) {
		this.message = message;
	}
	public String getImg() {
		return "http://www.yi18.net/"+img;
	}
	public void setImg(String img) {
		this.img = img;
	}
	public int getCount() {
		return count;
	}
	public void setCount(int count) {
		this.count = count;
	}
	public String getAuthor() {
		return author;
	}
	public void setAuthor(String author) {
		this.author = author;
	}
	public int getFocal() {
		return focal;
	}
	public void setFocal(int focal) {
		this.focal = focal;
	}
	public Date getTime() {
		return time;
	}
	public void setTime(Date time) {
		this.time = time;
	}
	
	

}
